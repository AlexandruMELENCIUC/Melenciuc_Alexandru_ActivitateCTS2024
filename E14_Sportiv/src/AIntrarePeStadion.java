public abstract class AIntrarePeStadion {
    abstract void asazaLaCoada();
    abstract void prezintaBiletul();
    abstract void controlCorporal();
    abstract void intraInStadion();
    abstract void ocupaLocul();

    public final void executaIntrarePeStadion() {
        asazaLaCoada();
        prezintaBiletul();
        controlCorporal();
        intraInStadion();
        ocupaLocul();
    }
}
